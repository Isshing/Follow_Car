void OLED_ShowChar(u8 x2, u8 y2, u8 chr, u8 Char_Size)
{
    unsigned char c = 0, i = 0;
    c = chr - ' ';
    if (x2 > Max_Column - 1)
    {
        x2 = 0;
        y2 = y2 + 2;
    }
    if (Char_Size == 16)
    {
        OLED_Set_Pos(x2, y2);
        for (i = 0; i < 8; i++)
            OLED_WR_Byte(F8X16[c * 16 + i], OLED_DATA);
        OLED_Set_Pos(x2, y2 + 1);
        for (i = 0; i < 8; i++)
            OLED_WR_Byte(F8X16[c * 16 + i + 8], OLED_DATA);
    }
    else
    {
        OLED_Set_Pos(x2, y2);
        for (i = 0; i < 6; i++)
            OLED_WR_Byte(F6x8[c][i], OLED_DATA);
    }
}

u32 oled_pow(u8 m, u8 n)
{
    u32 result = 1;
    while (n--)
        result *= m;
    return result;
}

void OLED_ShowNum(u8 x1, u8 y1, u32 num, u8 len, u8 size2)
{
    u8 t, temp;
    u8 enshow = 0;
    for (t = 0; t < len; t++)
    {
        temp = (num / oled_pow(10, len - t - 1)) % 10;
        if (enshow == 0 && t < (len - 1))
        {
            if (temp == 0)
            {
                OLED_ShowChar(x1 + (size2 / 2) * t, y1, ' ', size2);
                continue;
            }
            else
                enshow = 1;
        }
        OLED_ShowChar(x1 + (size2 / 2) * t, y1, temp + '0', size2);
    }
}
void OLED_ShowVI(u8 x, u8 y, u32 num, u8 size2)
{
    OLED_ShowNum(x + size2 * 4 - 1, y, num % 10, 1, size2);
    OLED_ShowNum(x + size2 * 3 - 1, y, num / 10 % 10, 1, size2);
    OLED_ShowNum(x + size2 * 2 - 1, y, num / 100 % 10, 1, size2);
    OLED_ShowString(x + size2 * 1 - 1, y, ".", size2);
    OLED_ShowNum(x - 1, y, num / 1000, 1, size2);
}

void OLED_ShowBNum(u8 x, u8 y, float num, u8 len, u8 size2) // len:带小数点、负号数据个数
{
    u8 t, temp, i = 0;
    u8 enshow = 0;
    u16 k;
    if (num < 0)
    {
        num = -num;
        i = 1; // 负数标志
    }
    k = num * 10; // 此处为显示一位小数*10转化为整数，若要显示几位就@我（自己想办法）
    for (t = 0; t < len; t++)
    {
        temp = (k / oled_pow(10, len - t - 1)) % 10;
        if (enshow == 0 && t < (len - 1))
        {
            if (temp == 0)
            {
                if (((k / oled_pow(10, len - t - 2) % 10) != 0) && (i == 1)) // 判断是否为负数且在最高位前一位
                {
                    OLED_ShowChar(x + (size2 / 2) * t, y, '-', size2);
                    i = 0; // 清除判断后一位的标志
                }
                else
                    OLED_ShowChar(x + (size2 / 2) * t, y, ' ', size2); // 如果没到数字就显示空格
                continue;
            }
            else
                enshow = 1; // 此处是判断是否要显示数字
        }
        if (t == len - 1)
        {
            OLED_ShowChar(x + (size2 / 2) * t, y, '.', size2);
            // 判断是否为最后一位的前一位（显示一位小数）
            t++;
        }
        OLED_ShowChar(x + (size2 / 2) * t, y, temp + '0', size2); // 一位一位显示下去
    }
}

void OLED_ShowString(u8 x3, u8 y3, u8 *chr, u8 Char_Size)
{
    unsigned char j = 0;
    while (chr[j] != '\0')
    {
        OLED_ShowChar(x3, y3, chr[j], Char_Size);
        x3 += 6;
        if (x3 > 120)
        {
            x3 = 0;
            y3 += 2;
        }
        j++;
    }
}

